	private static function _filename($type)
	{
		$a = explode('_', __CLASS__);
		$name = end($a);
		return __DIR__ . 'WordsParser.php/' . $name . '.' . $type . '.php';
	}

	private static function _re_langs($langs)
	{
		$a = [];
		foreach ($langs as $lang) {
            $a[] = '\p{' . preg_quote($lang, '~') . '}++';
        }

		return implode($a, ' | ');
	}
